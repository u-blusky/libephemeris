"""
VSOP87 planetary theory implementation for Moshier ephemeris.

Implements a truncated version of the VSOP87 theory for calculating
heliocentric positions of the major planets (Mercury through Neptune).

The VSOP87 theory (Variations Séculaires des Orbites Planétaires) was developed
by Pierre Bretagnon and Gérard Francou at the Bureau des Longitudes, Paris.

This implementation uses truncated series to achieve arcsecond-level accuracy
while maintaining reasonable code size. Full VSOP87 has thousands of terms;
we include only the most significant ones.

This module has NO dependencies on Skyfield or SPK files.
Only numpy is used for numerical operations.

References:
- Bretagnon, P. and Francou, G. (1988), "Planetary theories in rectangular
  and spherical variables. VSOP87 solutions", A&A 202, 309-315
- Meeus, J. (1991), "Astronomical Algorithms", Chapter 32
"""

