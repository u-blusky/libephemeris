def _evaluate_vsop_series(
    coord_series: VSOPCoord,
    tau: float,
) -> float:
    """Evaluate a VSOP87 coordinate series.

    Args:
        coord_series: List of term lists for each power of tau.
        tau: Julian millennia from J2000.0.

    Returns:
        Coordinate value in radians (L, B) or AU (R).
    """
    result = 0.0
    tau_power = 1.0

    for power, terms in enumerate(coord_series):
        term_sum = 0.0
        for A, B, C in terms:
            term_sum += A * math.cos(B + C * tau)
        result += term_sum * tau_power
        tau_power *= tau

    return result


def calc_vsop87_heliocentric(
    jd_tt: float,
    planet_idx: int,
) -> Tuple[float, float, float]:
    """Calculate heliocentric ecliptic coordinates using VSOP87.

    Args:
        jd_tt: Julian Day in Terrestrial Time.
        planet_idx: VSOP87 planet index (0-7 for Mercury through Neptune).

    Returns:
        Tuple of (longitude, latitude, radius) where:
        - longitude, latitude in degrees
        - radius in AU
    """
    # Julian millennia from J2000.0
    tau = jd_to_julian_millennia(jd_tt)

    # Get planet data
    vsop_data = _get_vsop87_data()
    if planet_idx not in vsop_data:
        raise ValueError(f"Invalid planet index: {planet_idx}")

    L_series, B_series, R_series = vsop_data[planet_idx]

    # Evaluate series
    # VSOP87 coefficients are in units of 10^-8 radians for L and B
    L = _evaluate_vsop_series(L_series, tau) * 1e-8  # radians
    B = _evaluate_vsop_series(B_series, tau) * 1e-8  # radians
    R = _evaluate_vsop_series(R_series, tau) * 1e-8  # AU

    # Convert to degrees
    L_deg = normalize_angle(L * RAD_TO_DEG)
    B_deg = B * RAD_TO_DEG

    return L_deg, B_deg, R


def calc_earth_heliocentric(jd_tt: float) -> Tuple[float, float, float]:
    """Calculate heliocentric coordinates of Earth.

    Args:
        jd_tt: Julian Day in Terrestrial Time.

    Returns:
        Tuple of (longitude, latitude, radius) in degrees and AU.
    """
    return calc_vsop87_heliocentric(jd_tt, _EARTH)


def calc_sun_geocentric(jd_tt: float) -> Tuple[float, float, float]:
    """Calculate geocentric coordinates of the Sun.

    The geocentric Sun is the heliocentric Earth + 180°.

    Args:
        jd_tt: Julian Day in Terrestrial Time.

    Returns:
        Tuple of (longitude, latitude, radius) in degrees and AU.
    """
    L, B, R = calc_earth_heliocentric(jd_tt)
    # Geocentric Sun is opposite to heliocentric Earth
    sun_L = normalize_angle(L + 180.0)
    sun_B = -B  # Latitude inverts
    return sun_L, sun_B, R


def calc_planet_geocentric(
    jd_tt: float,
    planet_idx: int,
) -> Tuple[float, float, float]:
    """Calculate geocentric ecliptic coordinates of a planet.

    Args:
        jd_tt: Julian Day in Terrestrial Time.
        planet_idx: VSOP87 planet index.

    Returns:
        Tuple of (longitude, latitude, distance) in degrees and AU.
    """
    # Get heliocentric coordinates of planet and Earth
    pL, pB, pR = calc_vsop87_heliocentric(jd_tt, planet_idx)
    eL, eB, eR = calc_earth_heliocentric(jd_tt)

    # Convert to rectangular
    pL_rad = pL * DEG_TO_RAD
    pB_rad = pB * DEG_TO_RAD
    eL_rad = eL * DEG_TO_RAD
    eB_rad = eB * DEG_TO_RAD

    # Planet heliocentric rectangular
    px = pR * math.cos(pB_rad) * math.cos(pL_rad)
    py = pR * math.cos(pB_rad) * math.sin(pL_rad)
    pz = pR * math.sin(pB_rad)

    # Earth heliocentric rectangular
    ex = eR * math.cos(eB_rad) * math.cos(eL_rad)
    ey = eR * math.cos(eB_rad) * math.sin(eL_rad)
    ez = eR * math.sin(eB_rad)

    # Geocentric rectangular
    gx = px - ex
    gy = py - ey
    gz = pz - ez

    # Convert back to spherical
    lon, lat, dist = cartesian_to_spherical(gx, gy, gz)

    return lon, lat, dist


