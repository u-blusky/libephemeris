def calc_position(
    jd_tt: float,
    body_id: int,
) -> Tuple[float, float, float, float, float, float]:
    """Calculate geocentric ecliptic position for a VSOP87 planet.

    This is the main entry point for planetary calculations.

    Args:
        jd_tt: Julian Day in Terrestrial Time.
        body_id: Swiss Ephemeris body ID (SE_MERCURY through SE_NEPTUNE).

    Returns:
        Tuple of (lon, lat, dist, dlon, dlat, ddist) where:
        - lon, lat in degrees (ecliptic J2000.0)
        - dist in AU
        - dlon, dlat in degrees/day
        - ddist in AU/day

    Raises:
        ValueError: If body_id is not a VSOP87 planet.
    """
    # Map SE body ID to VSOP index
    if body_id == MOSHIER_SUN:
        # Sun is a special case
        lon, lat, dist = calc_sun_geocentric(jd_tt)
    elif body_id in _SE_TO_VSOP:
        planet_idx = _SE_TO_VSOP[body_id]
        lon, lat, dist = calc_planet_geocentric(jd_tt, planet_idx)
    else:
        raise ValueError(f"Body {body_id} is not a VSOP87 planet")

    # Calculate velocities using numerical differentiation
    h = 0.001  # 1.44 minutes step
    if body_id == MOSHIER_SUN:
        pos_plus = calc_sun_geocentric(jd_tt + h)
        pos_minus = calc_sun_geocentric(jd_tt - h)
    else:
        planet_idx = _SE_TO_VSOP[body_id]
        pos_plus = calc_planet_geocentric(jd_tt + h, planet_idx)
        pos_minus = calc_planet_geocentric(jd_tt - h, planet_idx)

    dlon = pos_plus[0] - pos_minus[0]
    dlat = pos_plus[1] - pos_minus[1]
    ddist = pos_plus[2] - pos_minus[2]

    # Handle longitude wrap-around
    if dlon > 180.0:
        dlon -= 360.0
    elif dlon < -180.0:
        dlon += 360.0

    dlon /= 2 * h
    dlat /= 2 * h
    ddist /= 2 * h

    return lon, lat, dist, dlon, dlat, ddist


def is_vsop_body(body_id: int) -> bool:
    """Check if a body ID is handled by VSOP87.

    Args:
        body_id: Swiss Ephemeris body ID.

    Returns:
        True if body can be calculated with VSOP87.
    """
    return body_id == MOSHIER_SUN or body_id in _SE_TO_VSOP
