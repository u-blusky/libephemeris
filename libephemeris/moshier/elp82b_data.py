"""
ELP 2000-82B coefficient data for lunar theory.

This module contains the truncated coefficient tables for the ELP 2000-82B
lunar theory, based on Moshier's implementation used in Swiss Ephemeris.

The ELP theory computes the Moon's geocentric position as series:
- Longitude: L = L0 + sum(A * sin(i1*D + i2*M + i3*M' + i4*F))
- Latitude:  B = sum(A * sin(i1*D + i2*M + i3*M' + i4*F))
- Distance:  R = R0 + sum(A * cos(i1*D + i2*M + i3*M' + i4*F))

Where:
- D = Mean elongation of Moon from Sun
- M = Sun's mean anomaly
- M' = Moon's mean anomaly
- F = Moon's argument of latitude
- Omega = Longitude of ascending node (for some terms)

The coefficients include:
1. Main ELP terms (~470 longitude, ~270 latitude, ~170 distance)
2. Planetary perturbation terms (~200 additional)

Reference:
- Chapront-Touze, M. and Chapront, J. (1983), "The lunar ephemeris ELP 2000",
  Astronomy and Astrophysics, 124, 50-62
- Chapront-Touze, M. and Chapront, J. (1988), "ELP 2000-85: a semi-analytical
  lunar ephemeris adequate for historical times", A&A 190, 342
- Moshier, S.L., "Astronomical Ephemeris and Reduction Library"
"""

